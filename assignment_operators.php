<?php

echo "<h2>Assignment Operators in PHP</h2>";
$a=10; //assigning value 10 to variable a using = operator.

echo "Initial value = $a <br>";

$a += 5; //adding 5 to variable a using += operator.
echo "After adding 5 = $a <br>";

$a -= 3; //subtracting 3 from variable a using -= operator.
echo "After subtracting 3 = $a <br>";

$a *=2; //multiplying variable a by 2 using *= operator.
echo "After multiplying by 2 = $a <br>";

$a /=4; //dividing variable a by 4 using /= operator.
echo "After dividing by 4 = $a <br>";

$a %=3; //modulus of variable a by 3 using %= operator.
echo "After modulus by 3 = $a <br>";

$a **=2; //exponent of variable a by 2 using **= opersator.
echo "After exponent by 2 = $a <br>";

?>